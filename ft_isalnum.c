int	ft_isalnum(int letter)
{
	if ((letter >= 'A' && letter <= 'Z')
		|| (letter >= 'a' && letter <= 'z')
		|| (letter >= '0' && letter <= '9'))
	{
		return (1);
	}
	return (0);
}
